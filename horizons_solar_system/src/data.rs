#[allow(clippy::excessive_precision)]
pub static GRAVITATIONAL_PARAMETERS: LazyLock<std::collections::HashMap<i32, f64>> =
    LazyLock::new(|| {
        fn parse_gm_file(
            contents: &str,
        ) -> Result<std::collections::HashMap<i32, f64>, Box<dyn std::error::Error>> {
            contents
                .lines()
                .filter_map(|line| {
                    let b = line.find("BODY")?;
                    let gm = line.find("_GM")?;
                    let po = line.find('(')?;
                    let pc = line.find(')')?;

                    Some((|| {
                        let id = line[b + 4..gm].parse::<i32>()?;
                        let val = &line[po + 1..pc].trim();
                        let val = val.replace('D', "E");
                        let val = val.parse::<f64>()?;

                        Ok((id, val))
                    })())
                })
                .collect()
        }

        parse_gm_file(include_str!("gm_de440.tpc"))
            .unwrap_or_else(|_| panic!("Failed to parse gm_de440.tpc file"))
    });

